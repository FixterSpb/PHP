<?php

/*
 * Объявить массив, индексами которого являются буквы русского языка,
 * а значениями – соответствующие латинские буквосочетания
 * (‘а’=> ’a’, ‘б’ => ‘b’, ‘в’ => ‘v’, ‘г’ => ‘g’, …, ‘э’ => ‘e’, ‘ю’ => ‘yu’, ‘я’ => ‘ya’).
 * Написать функцию транслитерации строк.

 */

function translit($str){
    $letters = [
        'а' => 'a',
        'б' => 'b',
        'в' => 'v',
        'г' => 'g',
        'д' => 'd',
        'е' => 'e',
        'ё' => 'yo',
        'ж' => 'zh',
        'з' => 'z',
        'и' => 'i',
        'й' => 'y',
        'к' => 'k',
        'л' => 'l',
        'м' => 'm',
        'н' => 'n',
        'о' => 'o',
        'п' => 'p',
        'р' => 'r',
        'с' => 's',
        'т' => 't',
        'у' => 'u',
        'ф' => 'f',
        'х' => 'h',
        'ц' => 'c',
        'ч' => 'ch',
        'ш' => 'sh',
        'щ' => 'shch',
        'ъ' => "\"",
        'ы' => 'y',
        'ь' => '′',
        'э' => 'e',
        'ю' => 'yu',
        'я' => 'ya'
    ];
    $newStr = '';
    for ($i = 0; $i < strlen($str); $i++){
        $symbol = mb_substr(mb_strtolower($str), $i, 1, "utf8");
        if (isset($letters[$symbol])){
            $newStr .= $letters[$symbol];
        }else{
            $newStr .= $symbol;
        }
    }

    return $newStr;
}
echo translit('Проверка задания № 4.<br>');